# cek server hidup atau tidak
@app.route('/', methods=['GET'])
def root():
    return jsonify({
        'message': 'WorkSafe AI API aktif',
        'version': '1.0.0'
    })

# endpoint utama prediksi
@app.route('/predict', methods=['POST'])
def predict():
    try:
        data = request.get_json()

        if not data or 'features' not in data:
            return jsonify({'error': 'Key features tidak ditemukan'}), 400

        # isi fitur yang tidak dikirim dengan nilai median
        full_input = {col: median_values[col] for col in feature_cols}
        full_input.update(data['features'])

        X     = preprocess(full_input)
        proba = model.predict(X, verbose=0)[0]
        kelas = int(np.argmax(proba))

        return jsonify({
            'risk_label'   : LABEL_MAP[kelas],
            'risk_class'   : kelas,
            'confidence'   : round(float(proba[kelas]) * 100, 2),
            'probabilities': {
                'Low Risk'   : round(float(proba[0]) * 100, 2),
                'Medium Risk': round(float(proba[1]) * 100, 2),
                'High Risk'  : round(float(proba[2]) * 100, 2),
            }
        })

    except Exception as e:
        return jsonify({'error': str(e)}), 500

# endpoint untuk lihat daftar fitur yang dibutuhkan model
@app.route('/features', methods=['GET'])
def get_features():
    return jsonify({
        'total_features': len(feature_cols),
        'feature_list'  : list(feature_cols)
    })

