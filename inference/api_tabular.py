from flask import Flask, request, jsonify
import numpy as np
import pandas as pd
import pickle
import tensorflow as tf
from tensorflow import keras

app = Flask(__name__)

PATH_MODEL    = './models/worksafe_model_v1.keras'
PATH_SCALER   = './models/scaler.pkl'
PATH_IMPUTER  = './models/imputer.pkl'
PATH_FEATURES = './models/feature_cols.pkl'

# ============================================================
# FOCAL LOSS
# ============================================================
class FocalLoss(keras.losses.Loss):
    def __init__(self, gamma=2.0, alpha=0.25, name='focal_loss', **kwargs):
        super().__init__(name=name, **kwargs)
        self.gamma = gamma
        self.alpha = alpha

    def call(self, y_true, y_pred):
        y_true    = tf.cast(tf.reshape(y_true, [-1]), tf.int32)
        y_true_oh = tf.cast(tf.one_hot(y_true, depth=3), tf.float32)
        y_pred    = tf.cast(y_pred, tf.float32)
        y_pred    = tf.clip_by_value(y_pred, 1e-7, 1.0 - 1e-7)
        ce           = -y_true_oh * tf.math.log(y_pred)
        p_t          = tf.reduce_sum(y_true_oh * y_pred, axis=-1, keepdims=True)
        focal_weight = tf.pow(1.0 - p_t, self.gamma)
        loss         = self.alpha * focal_weight * tf.reduce_sum(ce, axis=-1)
        return tf.reduce_mean(loss)

    def get_config(self):
        cfg = super().get_config()
        cfg.update({'gamma': self.gamma, 'alpha': self.alpha})
        return cfg

# ============================================================
# LOAD SEMUA ARTIFACT SAAT SERVER NYALA
# ============================================================
model = keras.models.load_model(
    PATH_MODEL,
    custom_objects={'FocalLoss': FocalLoss}
)
with open(PATH_SCALER,   'rb') as f: scaler       = pickle.load(f)
with open(PATH_IMPUTER,  'rb') as f: imputer      = pickle.load(f)
with open(PATH_FEATURES, 'rb') as f: feature_cols = pickle.load(f)

LABEL_MAP = {0: 'Low Risk', 1: 'Medium Risk', 2: 'High Risk'}

median_values = {
    col: float(imputer.statistics_[i])
    for i, col in enumerate(feature_cols)
}

print("Model loaded. Server siap.")

# ============================================================
# FUNGSI PREPROCESSING
# ============================================================
def preprocess(input_dict):
    df = pd.DataFrame([input_dict])
    for col in feature_cols:
        if col not in df.columns:
            df[col] = np.nan
    df     = df[feature_cols]
    df_imp = pd.DataFrame(
        imputer.transform(df),
        columns=feature_cols
    )
    df_scaled = pd.DataFrame(
        scaler.transform(df_imp),
        columns=feature_cols
    )
    return df_scaled.values.astype(np.float32)

